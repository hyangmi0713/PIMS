package stock;

public class Stock {
	private int stockNo;
	private int stockPartNo;
	private int stockProjectNo;
	private int stockPaperNo;
	private int stockPaperStock;
	
	public int getStockNo() {
		return stockNo;
	}
	public void setStockNo(int stockNo) {
		this.stockNo = stockNo;
	}
	public int getStockPartNo() {
		return stockPartNo;
	}
	public void setStockPartNo(int stockPartNo) {
		this.stockPartNo = stockPartNo;
	}
	public int getStockProjectNo() {
		return stockProjectNo;
	}
	public void setStockProjectNo(int stockProjectNo) {
		this.stockProjectNo = stockProjectNo;
	}
	public int getStockPaperNo() {
		return stockPaperNo;
	}
	public void setStockPaperNo(int stockPaperNo) {
		this.stockPaperNo = stockPaperNo;
	}
	public int getStockPaperStock() {
		return stockPaperStock;
	}
	public void setStockPaperStock(int stockPaperStock) {
		this.stockPaperStock = stockPaperStock;
	}
}
