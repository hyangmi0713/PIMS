package graphy;

public class GraphY {
	private int graphNo;
	private String graphName;
	private int graphStockSum;
	
	public int getGraphNo() {
		return graphNo;
	}
	public void setGraphNo(int graphNo) {
		this.graphNo = graphNo;
	}
	public String getGraphName() {
		return graphName;
	}
	public void setGraphName(String graphName) {
		this.graphName = graphName;
	}
	public int getGraphStockSum() {
		return graphStockSum;
	}
	public void setGraphStockSum(int graphStockSum) {
		this.graphStockSum = graphStockSum;
	}
}
