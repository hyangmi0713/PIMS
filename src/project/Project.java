package project;

public class Project {
	private int projectNo;
	private String projectName;
	private int projectPartNo;
	private String projectPartName;
	private String projectPaperListName;
	public int getProjectNo() {
		return projectNo;
	}
	public void setProjectNo(int projectNo) {
		this.projectNo = projectNo;
	}
	public String getProjectName() {
		return projectName;
	}
	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}
	public int getProjectPartNo() {
		return projectPartNo;
	}
	public void setProjectPartNo(int projectPartNo) {
		this.projectPartNo = projectPartNo;
	}
	public String getProjectPartName() {
		return projectPartName;
	}
	public void setProjectPartName(String projectPartName) {
		this.projectPartName = projectPartName;
	}
	public String getProjectPaperListName() {
		return projectPaperListName;
	}
	public void setProjectPaperListName(String projectPaperListName) {
		this.projectPaperListName = projectPaperListName;
	}
	
	
}
